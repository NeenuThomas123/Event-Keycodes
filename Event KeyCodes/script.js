//get html insert
const insert = document.getElementById('insert')

//listen for any key press anywhere on the page
window.addEventListener('keydown', (event) => {

  //inside that function build three little boxes
  insert.innerHTML = `
  <div class="key">
  ${event.key === ' ' ? 'Space' : event.key} 
  <small>event.key</small>
</div>

<div class="key">
  ${event.keyCode}
  <small>event.keyCode</small>
</div>

<div class="key">
  ${event.code}
  <small>event.code</small>
</div>
  `
})